package com.nsdl.telemedicine.review.constant;
/**
 * @author Pegasus_girishk
 *
 */
public enum ErrorConstants {

	INVALID_USER_INPUT("PW-0001", "Invalid User Input"),
	SOMETHING_WENT_WRONG("PW-0002","Something went wrong, Please contact admin."),
	NO_REVIEWS_FOUND("PW-0003","No Reviews Found"),
	INVALID_RATING("PW-0004","Patient should rate from 1 to 10.");
	
	private String code;
	private String message;
	private ErrorConstants(String code, String message) {
		this.code = code;
		this.message = message;
	}

	public String getCode() {
		return code;
	}

	public String getMessage() {
		return message;
	}
	
}
