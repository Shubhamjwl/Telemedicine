package com.nsdl.auth.constant;

public enum DrVerificationStatus {
	REGISTERED("R", "REGISTERED"), APPROVED("A", "APPROVED"), NOTVERIFIED("N", "NOT VERIFIED"),
	VERIFIED("V", "VERIFIED");

	private String status;
	private String statusName;

	private DrVerificationStatus(String status, String statusName) {
		this.status = status;
		this.statusName = statusName;
	}

	public String getStatus() {
		return status;
	}

	public String getStatusName() {
		return statusName;
	}

}
