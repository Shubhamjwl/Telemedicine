package com.nsdl.ndhm.dto;

public class OtpRequestDTO {
	private String userId;
	private String mobileNo;
	private String emailID;
	private String sendType;
	private String otpGenerateTpye;
	private String otpFor;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getEmailID() {
		return emailID;
	}

	public void setEmailID(String emailID) {
		this.emailID = emailID;
	}

	public String getSendType() {
		return sendType;
	}

	public void setSendType(String sendType) {
		this.sendType = sendType;
	}

	public String getOtpGenerateTpye() {
		return otpGenerateTpye;
	}

	public void setOtpGenerateTpye(String otpGenerateTpye) {
		this.otpGenerateTpye = otpGenerateTpye;
	}

	public String getOtpFor() {
		return otpFor;
	}

	public void setOtpFor(String otpFor) {
		this.otpFor = otpFor;
	}
}
